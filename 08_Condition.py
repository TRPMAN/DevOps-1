# If Condition
Number = 4
if Number >= 8:
    print("Inside IF Block")
    print("Number is higher or equal to 8")
print("The rest of the code")

# If-else
Number = 10
if Number >= 8:
    print("Inside IF Block")
    print("Number is higher or equal to 8")
else:
    print("Inside ELSE Block")
    print("Number is lower than 8")

# If-elif-else
Number = 8
if Number > 8:
    print("Inside IF Block")
    print("Number is higher than 8")
elif Number == 8:
    print("Inside ELSE Block")
    print("Number is equal to 8")
else:
    print("Inside ELSE Block")
    print("Number is lower than 8")